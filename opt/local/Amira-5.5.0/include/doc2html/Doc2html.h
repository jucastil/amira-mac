#ifndef DOC_OUTPUT_H
#define DOC_OUTPUT_H

#include <mclib/McDArray.h>
#include <mclib/McString.h>
#include <QHash>
#include <qfileinfo.h>

#include <map>

struct DocFile;
extern McDArray<DocFile*> docStack;
extern McFilename currentInputFile;
extern McDArray<McString> allInputFiles;



///
class Doc2html
{
  public:
  
    enum {
        autoModeNone = 1 << 0,
        autoModeAllAvailable = 1 << 1,
        autoModeRoot = 1 << 2,
        autoModeLocal = 1 << 3
    };  

    /// Constructor.
    Doc2html(const char* path);

    /// Destructor
    ~Doc2html();

    ///
    void setVerbosity(int level) { verbosity = level; }
    
    ///
    void setAutoMode(int mode) { automode = mode; }
    
    ///
    int getAutoMode() { return automode; }    

    ///
    void setIndexDirectory(const char* dir){ indexDirectory = dir; }

    ///
    const char* getIndexDirectory();
    
    ///
    void setProductName(const char* name){ productName = name; }

    ///
    McString getProductName();  
    
    /// return MAJOR.MINOR
    McString getProductVersion();  
    
    /// return MAJOR
    McString getProductMajorVersion();      

    ///
    void setOutputDirectory(const char* dir){ outputDirectory = dir; outputDirectory.canonizePath(); }

    static const int outDirRoot = 0;
    static const int outDirLocal = 1;
    static const int outDirRootOverride = 2;

    /// get current output directory (depending on outputDirMode this
    /// will return:
    /// const int outDirRoot = 0;  -> AMIRA_ROOT../share/doc/amira
    /// const int outDirLocal = 1; -> AMIRA_LOCAL/share/doc/amira
    /// const int outDirRootOverride = 2; ->
    ///    if a root dir was given by the -d parameter, this dir is returned.
    ///    otherwise return AMIRA_ROOT../share/doc/amira
    const char* getOutputDirectory(int outputDirMode);
    
    /// get current output directory (depending on current autoMode this
    /// will return either the outDirLocal or outDirRootOverride directory)
    const char* getOutputDirectory();    
    
    
protected:
    /// auto-generate output directory (depending on autoMode)
    McFilename* generateOutputDirectory(int outputDirMode);
    
public:    
    ///
    const char* getMathjaxDirectory();

    ///
    const char* getCSSPath();

    ///
    const char* getCurrentInputFilename() const;

    ///
    const char* getCurrentInputDirectory() const;

    ///
    void addImageDirectory(const char* dir) { imageDirs.append(McString(dir)); }

    ///
    const char* getRootDir();

    ///
    void setRootDir(const char* root);

    ///
    const char* getLocalDir();

    ///
    void setLocalDir(const char* local);
    
    ///
    void setInputPackageNames(McDArray<McFilename> *packages);

    ///
    int isVerbose(int level=2) const { return (verbosity>=level); }

    ///
    FILE* open(const char* filename);

    ///
    int close();

    ///
    void exclude();

    ///
    void findAllDocFiles(McDArray<McFilename>& docFiles);

    /// Appends all doc files found below directory dir to docFiles.
    void findAllDocFiles(QDir dir, McDArray<McFilename>& docFiles);

    ///
    void findLocalDocFiles(McDArray<McFilename>& docFiles);

    /** return true if any references that latex can't find could
        be resolved using the internal label list. This shows that
        another latex run would resolve these references. */
    bool checkForSecondRun(McDArray<McFilename>& files);

    ///
    void resetCounters();

    ///
    McString makeRelative(const char* dir);


    void initAliasMap();
    
    bool writeStyleFile(McFilename styFile);



    /** Activate index mode (index files are created in specified
        directory). The packageList is a file with a list of
        packages. Each line should start with the package name,
        optional followed by -arch-ANYTHING. Lines starting with # are
        ignored. If the packagelist is set
        with a valid package file, only modules contained in one of
        these packages will appear in the Index files. */
    void setIndexMode(const char* indexDirectory, const char* packageList=0);





    void print(const char* text);

    void printf(const char* format, ...);

    void log(int level, const char* format, ...);

    McString* newChapter(const char* title, int enumerate=1);

    McString* newSection(const char* title, int enumerate=1);

    McString* newSubSection(const char* title, int enumerate=1);

    McString* newSubSubSection(const char* title, int enumerate=1);

    McString* newSubSubSubSection(const char* title, int enumerate=1);
    
    McString* publicationHint();
    
    McString* newModule(const char* packName, const char* name, const char* memo, const char* label);
    McString* newDataType(const char* packName, const char* name, const char* memo, const char* label);
    McString* newComponent(const char* packName, const char* name, const char* memo, const char* label);
    McString* newEditor(const char* packName, const char* name, const char* memo, const char* label);
    McString* newDemo(const char* packName, const char* name, const char* memo, const char* label);
    McString* newFileFormat(const char* packName, const char* name, const char* memo, const char* label);
    McString* newZIBTutPub(const char* packName, const char* name, const char* memo, const char* label);
    McString* newZIBTutProj(const char* packName, const char* name, const char* memo, const char* label);
    McString* newGLSLShader(const char* packName, const char* name, const char* memo, const char* label);

    void newFigure();

    McString* caption(McString* title);

    void push(char* str);

    void pushAdd(char* str);

    void pop();
    
    // copy an image from package tree to html doc tree
    McFilename* copyImage(const char* srcfile);
    
    // write a subpage which contains only the image and a caption
    McFilename* writeImageSubPage(McFilename* image, McString* caption);

    // Defines a label for the last mark (chapter, section, figure, ...).
    void label(const char* label);

    // Inserts an invisible mark into the document (HTML anchor).
    McString* hxlabel(const char* key);

		// Inserts an invisible mark into the document (HTML anchor) around the text.
		McString* hxlabel(const char* key, const char* text);
		
    // Extracts the first <a name>...</a> occurence from text (used when hxlabel was misplaced) 
    McString* extractFirstAnchor(McString* text);

    // References structural element indicated by #label#.
    McString* ref(const char* label, const char* preTxt=0, const char* postTxt=0);

    // Creates link to #label#, but no LaTeX ref.
    McString* link(const char* key, const char* text);

    // Prints table of contents.
    McString* writeTableOfContents();
    
    // List of generated index files
    McDArray<McString> addedIdxFilesList;

    /// Adds an entry to specified index (used to create reference guide).
    void addToIndex(const char* index, const char* packName, const char* name, const char* memo, const char* href=0);

    void replaceAliasNames(McString& str, bool decorate = true);

    ///
    void writeIndexFiles();
    void writeIndexFilesForPDF();

    ///
    void readAuxFile(bool all=true);

    ///
    void writeAuxFile();

    ///
    void setPNG(int usePNG,int bgCol[3]);

    ///
    void printUnresolvedReferences();

    ///
    void getPackages(McDArray<McString>& p) { p=packages; }


    ///
    McString* insertBaseExtensions();


    void error(const char* fmt, ...);
    void warning(const char* fmt, ...);

    void clearAllInput();

    void registerExtension();

    void processExtensions(McDArray<McFilename>& inputFiles);

  protected:
    int verbosity;
    int automode;
    bool documentMode;

    QFileInfo progInfo;
    McString rootDir;  //AMIRA_ROOT
    McString localDir; //AMIRA_LOCAL
    McFilename indexDirectory;  //path to hxindex* files and index.doc
    McFilename outputDirectory; //store the output dir given by -d parameter
    McFilename outputDirectoryTemp;  //store other temporal output dirs
    McFilename mathjaxDirectory;  //path to mathjax
    QString currentDirectory;  //current input directory
    McDArray<McFilename> inputPackageNames; //list of packages to be translated
    
    McString productName;
    McString productVersion;
    McString productMajorVersion;

    void readAuxFile(const char* directory, bool all);
    void setTocEntry(int level, const char* num, const char* name, const char* href);
    int lock();
    int unlock();
    void unresolvedRefError(const char* ref, const char* epath=0);

    McFilename fixUnresolvedLinks(const char* ref, McFilename& epath);


    int figureCount;
    int chapterCount;
    int sectionCount;
    int subSectionCount;
    int subSubSectionCount;
    int subSubSubSectionCount;

    struct TocEntry {
        int level;
        McString num;
        McString name;
        McString href;
    };

    int currentTocPosition;
    McDArray<TocEntry*> tableOfContents;

    struct OutputFile {
        FILE* fp;
        McString basename;
        McString inputFile;
        int currentAnchor;
    };

    McString homeHRef;
    McString directory;
    McDArray<OutputFile*> files;

    McDArray<McString> stack;


    struct Label {
        McString labelText;
        McString href;
        McString inputFile;
    };

    int currentAnchorNumber;
    McString currentHRef;
    McString currentLabelText;
    McString currentCaptionText;
    QHash<QString, Label*> labels;

    struct UnresolvedReference {
        McString filename;
        int lineNumber;
        McString reference;
        McFilename epath; // ersatz path
        bool epathOk() const { return epath.length() && epath.doesExist(); }
    };

    McDArray<UnresolvedReference> unresolvedReferences;
    McDArray<DocFile*> fullDocStack;

    // This array contains a list of directories where images are searched
    McDArray<McString> imageDirs;

    struct Index {
        struct Entry {
            McFilename docFile;
            McString packName;
            McString title;
            McString memo;
        };
        McString name;
        int hasChanged;
        McDArray<Entry*> content;
    };

    /** Check whether entry should appear in index, i.e. whether it
        is part of one of the packages given in the package list.*/
    int isInIndex(Doc2html::Index::Entry& e);

    // list of package names to be considered for the index.
    McDArray<McString> packages;

    McDArray<Index*> indexList;
    McDArray<McString> packNameList;

public:

    // Alias are sorted by decreasing lengths, since we should replace \amiraXxxx before \amira
    struct ltstr{
      bool operator()(const McString & s1, const McString & s2) const{
        if(s2.length() == s1.length()) return s1 < s2;
        return s2.length() < s1.length();
      }
    };

    std::map<McString, McString, ltstr> aliasNameList;
    std::map<McString, McString, ltstr> aliasEnvList;

protected:

    static int indexCompare(Index::Entry* const& a, Index::Entry* const& b);

    McString* newGenericEntry(const char* header, const char* index, const char* packName,
                         const char* name, const char* memo, const char* label);

    McDArray<McString> extensions;
    McDArray<McFilename> excludedFiles;

    void sortItemList(McDArray<Doc2html::Index*>& indexList);
    void sortIndexList(McDArray<McString>& packNameList);
    void replacePackNames(McString& str);
    void removeHtml(QString& str);
    void removeHtml(McString* str);
};

extern Doc2html* docOutput;

#endif

